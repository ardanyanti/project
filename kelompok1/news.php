        <p>In early 2023, Indonesian pop star Mahalini officially marked her debut studio album with the release of Fábula. The album not only showcased her powerful vocals but also highlighted her versatility as a songwriter and storyteller.

Released digitally on January 23, 2023, Fábula quickly captured the hearts of fans across Indonesia and Southeast Asia. The album consists of 10 tracks, combining emotional ballads, catchy pop hooks, and sincere lyrics. Mahalini personally contributed to the songwriting process on most of the songs, collaborating with renowned Indonesian musicians and producers like Tintin, Sevri Hadis, Keke Kananta, and Andmesh Kamaleng.</p>

<!-- Tracklist -->
<h6><strong>Tracklist of Fábula:</strong></h6>
<ol>
    <li>01. Ini Laguku</li>
    <li>02. Buru-Buru</li>
    <li>03. Bawa Dia Kembali </li>
    <li>04. Putar Waktu</li>
    <li>05. Bohongi Hati</li>
    <li>06. Melawan Restu</li>
    <li>07. Sial</li>
    <li>08. Sisa Rasa</li>
    <li>09. Pecahkan Hatiku</li>
    <li>10. Kisah Sempurna</li>
</ol>

<p>Among the standout singles are Melawan Restu, Sisa Rasa, Sial, Kisah Sempurna, and Bohongi Hati, all of which gained massive streaming numbers on digital platforms. In fact, Fábula has surpassed 1 billion Spotify streams, a milestone that firmly established Mahalini as one of the leading pop artists of her generation.

The album’s title Fábula, which means “fable” or “tale”, reflects Mahalini’s vision of telling her personal and emotional journey through music. Her honest lyrics resonate deeply with listeners, touching on themes of love, heartbreak, and self-reflection.

Shortly after its digital release, Mahalini held a listening session at MNC Studios, Jakarta, giving media and fans a preview of the album’s full experience. In October 2023, Fábula was also released as a Limited Boxset CD Edition, which quickly became a collector’s item for fans.

Fábula’s commercial success was further recognized when it won Best Pop Album at the 2023 Anugerah Musik Indonesia (AMI Awards). It also dominated the charts, topping Spotify Indonesia for 34 consecutive weeks and making a strong debut on Spotify Malaysia’s album chart.

As a whole, Fábula stands as a testament to Mahalini’s growth from an Indonesian Idol finalist to a fully-fledged pop star. With heartfelt lyrics, impeccable vocals, and outstanding production, the album cements her place as one of the brightest names in Southeast Asia’s music industry.
</p>
